# Python Data Types â€” Basic Examples

# Integers: whole numbers
age = 22
year = 2025
print("Age:", age)
print("Year:", year)

# Floats: numbers with decimal points
height = 5.4
temperature = 36.6
print("Height:", height)
print("Temperature:", temperature)

# Strings: text values inside quotes
name = "Jagriti"
message = "Learning Python step by step"
print("Name:", name)
print("Message:", message)

# Booleans: True or False (mostly used in conditions)
is_student = True
is_graduated = False
print("Student:", is_student)
print("Graduated:", is_graduated)

# None type: represents 'no value'
result = None
print("Result is:", result)

# Checking the type of each variable
print("\n--- Data Types ---")
print(type(age))
print(type(height))
print(type(name))
print(type(is_student))
print(type(result))
