# Understanding Comments in Python

# A single-line comment starts with '#'
# It helps us remember why we wrote something or describe a line of code

print("Hello! This is Python commenting demo")  # This prints a message

# ------------------------------------------------------

# We also have multi-line comments.
# They are written using triple quotes and help us explain longer ideas.
"""
This is a multi-line comment.
You can write notes, explain logic,
or describe a whole section of code here.
"""

# Let's write a small block of code with comments explaining the intention

x = 10   # storing a number
y = 5    # another number

# Adding two values
result = x + y

print("Sum:", result)

# ------------------------------------------------------

"""
Useful when comments help:
- Explaining a logic or formula
- Describing what a function does
- Leaving reminders for future you
- Making code more beginner-friendly
"""

print("Comments make code readable and friendly!")
