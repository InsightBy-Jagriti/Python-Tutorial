# Bitwise Operators in Python

a = 5   # 0101
b = 3   # 0011

print("a =", a)
print("b =", b)

print("\n--- Bitwise AND ---")
print("a & b =", a & b)

print("\n--- Bitwise OR ---")
print("a | b =", a | b)

print("\n--- Bitwise XOR ---")
print("a ^ b =", a ^ b)

print("\n--- Bitwise NOT ---")
print("~a =", ~a)

print("\n--- Left Shift ---")
print("a << 1 =", a << 1)

print("\n--- Right Shift ---")
print("a >> 1 =", a >> 1)
